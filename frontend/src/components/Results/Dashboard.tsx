import React from 'react';
import Gauge from './Gauge';
import { generatePDF } from '../../utils/pdfGenerator';

interface MetricDetail {
    name: string;
    value: number;
    description: string;
    status: string;
}

interface AnalysisResult {
    overall_score: number;
    verdict: string;
    details: MetricDetail[];
    sentences: any[];
}

interface DashboardProps {
    result: AnalysisResult | null;
}

const Dashboard: React.FC<DashboardProps> = ({ result }) => {
    if (!result) return null;

    const isAI = result.overall_score > 50;
    const scoreColor = isAI ? 'text-rose-600' : 'text-emerald-600';
    const bgGradient = isAI ? 'from-rose-50 to-white' : 'from-emerald-50 to-white';

    const handleDownload = () => {
        generatePDF(result);
    };

    return (
        <section className="w-full max-w-5xl mx-auto mt-12 mb-20 px-4 animate-slide-up">

            <div className={`bg-gradient-to-br ${bgGradient} rounded-3xl border border-slate-200 shadow-xl shadow-slate-200/50 overflow-hidden`}>

                {/* Header Section */}
                <div className="p-8 md:p-12 border-b border-slate-100 flex flex-col md:flex-row items-center gap-12">

                    {/* Gauge Visual */}
                    <div className="relative">
                        <div className="absolute inset-0 bg-white/50 blur-3xl rounded-full"></div>
                        <Gauge
                            value={result.overall_score}
                            label="AI Probability"
                            color={isAI ? "text-rose-500" : "text-emerald-500"}
                        />
                    </div>

                    {/* Verdict Text */}
                    <div className="text-center md:text-left flex-1">
                        <div className="inline-flex items-center gap-2 px-3 py-1 rounded-full bg-white border border-slate-200 text-xs font-bold text-slate-500 mb-6 uppercase tracking-wider shadow-sm">
                            <span className={`w-2 h-2 rounded-full ${isAI ? 'bg-rose-500' : 'bg-emerald-500'}`}></span>
                            Analysis Complete
                        </div>

                        <h2 className={`text-5xl md:text-6xl font-bold mb-4 tracking-tight ${scoreColor}`}>
                            {result.verdict}
                        </h2>

                        <p className="text-slate-600 text-lg leading-relaxed max-w-lg">
                            Our multi-model analysis indicates a <strong className="text-slate-900">{result.overall_score}% probability</strong> that this content was generated by AI.
                            {isAI ? ' We recommend further verification for academic or professional use.' : ' This content appears to be human-written.'}
                        </p>
                    </div>
                </div>

                {/* Metrics Grid */}
                <div className="bg-white p-8 md:p-12">
                    <h3 className="text-sm font-bold text-slate-400 uppercase tracking-widest mb-8">Detailed Metrics</h3>

                    <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
                        {result.details.map((metric, idx) => (
                            <div key={idx} className="group p-6 rounded-2xl bg-slate-50 hover:bg-white border border-slate-100 hover:border-blue-100 hover:shadow-lg hover:shadow-blue-500/5 transition-all duration-300">
                                <div className="flex justify-between items-start mb-4">
                                    <div className="p-2 bg-white rounded-lg border border-slate-100 group-hover:border-blue-100 group-hover:scale-110 transition-all">
                                        {/* Icons based on metric name could go here, for now using generic shapes */}
                                        <div className={`w-4 h-4 rounded-sm ${metric.status.includes('AI') ? 'bg-rose-500' : 'bg-emerald-500'}`}></div>
                                    </div>
                                    <span className={`text-[10px] font-bold px-2 py-1 rounded-full uppercase tracking-wider ${metric.status.includes('AI')
                                        ? 'bg-rose-50 text-rose-600 border border-rose-100'
                                        : 'bg-emerald-50 text-emerald-600 border border-emerald-100'
                                        }`}>
                                        {metric.status}
                                    </span>
                                </div>

                                <div className="flex items-baseline gap-1 mb-2">
                                    <span className="text-3xl font-bold text-slate-900">{metric.value}</span>
                                    {(metric.name.includes("Score") || metric.name.includes("Confidence")) && (
                                        <span className="text-sm text-slate-400 font-medium">%</span>
                                    )}
                                </div>

                                <h4 className="font-semibold text-slate-700 mb-1">{metric.name}</h4>
                                <p className="text-xs text-slate-500 leading-relaxed">
                                    {metric.description}
                                </p>
                            </div>
                        ))}
                    </div>
                </div>

                {/* Footer Action */}
                <div className="bg-slate-900 p-8 flex flex-col md:flex-row items-center justify-between gap-6">
                    <div>
                        <h4 className="text-white font-bold text-lg mb-1">Need a signed certificate?</h4>
                        <p className="text-slate-400 text-sm">Download a verified PDF report for compliance purposes.</p>
                    </div>
                    <button
                        onClick={handleDownload}
                        className="px-6 py-3 bg-white hover:bg-blue-50 text-slate-900 font-bold rounded-xl text-sm transition-colors flex items-center gap-2"
                    >
                        <svg xmlns="http://www.w3.org/2000/svg" className="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
                        </svg>
                        Download Report
                    </button>
                </div>

            </div>
        </section>
    );
};

export default Dashboard;
